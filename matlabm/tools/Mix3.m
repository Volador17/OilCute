function[x,r1,r2,r3]=Mix3(x1,x2,x3);
% 计算混兑比例模块，三种样品混兑
% x，混兑后的模拟光谱，r1，r2，r3两种混兑组分x1, x2,x2的相应比例。
% x1，x2，x3,三种混兑组分的光谱，
sc1=[80 10 10;70 10 20;70 20 10;60 10 30;60 20 20;60 30 10];
sc2=[50 10 40;50 20 30;50 30 20;50 40 10;40 10 50;40 20 40];
sc3=[40 30 30;40 40 20;40 50 10;30 10 60;30 20 50;30 30 40;30 40 30;30 50 20;30 60 10;];
sc4=[20 10 70;20 20 60;20 30 50;20 40 40;20 50 30;20 60 20];
sc5=[20 70 10;10 10 80;10 20 70;10 30 60;10 40 50;10 50 40;10 60 30];
sc6=[10 70 20;10 80 10;];
sc7=[0 100 0;10 90 0;20 80 0;30 70 0;40 60 0;50 50 0;60 40 0;70 30 0;80 20 0;90 10 0;100 0 0];
sc8=[10 0 90;20 0 80;30 0 70;40 0 60;50 0 50;60 0 40;70 0 30;80 0 20;90 0 10;0 0 100];
sc9=[0 10 90;0 20 80;0 30 70;0 40 60;0 50 50;0 60 40;0 70 30;0 80 20;0 90 10];
sc=[sc1;sc2;sc3;sc4;sc5;sc6;sc7;sc8;sc9];
[m1,n1]=size(sc);
for i=1:m1
    x(:,i)=(x1*sc(i,1)+x2*sc(i,2)+x3*sc(i,3))/100;
    r1(i)=sc(i,1)';
    r2(i)=sc(i,2)';
    r3(i)=sc(i,3)';
end
